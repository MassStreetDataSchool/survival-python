'''
Variable Name Rules:
    1. Must start with a letter or an underscore
    2. Can only contain alphanumeric characters and underscores. (A-Z, a-z, 0-9 and _)
    3. Case sensitive
'''

# these are examples of valid variable names
food = 'Pasta'
Food = 'Macaroni'
FOOD = 'LASAGNA'
_food = 'Pene'
food1 = 'Fettuccine'

print(food)
print(Food)
print(FOOD)
print(_food)
print(food1)